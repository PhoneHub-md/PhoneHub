            $.ajax({
                url: 'MODELO/obtenerCarrito.php',
                type: 'GET',
                dataType: 'html',
                success: function(data) {
                    console.log('Información del carrito actualizada:');
                    $('#divCarrito').html(data);
                    $('#divCarritoResp').html(data);

