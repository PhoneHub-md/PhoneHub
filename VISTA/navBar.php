            $.ajax({
                type: 'POST',
                url: 'index.php',
                data: formData + '&accion=' + boton,
                dataType: 'json',
                success: function (response) {
                    if (response.status === 'success') {
                        console.log('Producto eliminado del carrito');
                        actualizarCarrito();
                    } else {
                        console.log('Error: ' + response.message);
                    }
                },
                error: function (error) {
                    console.error('Error:', error);
                }
            });
        }

